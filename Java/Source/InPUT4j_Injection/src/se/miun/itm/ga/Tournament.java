package se.miun.itm.ga;

public class Tournament extends Selection{

	private int size;
	private int winner;
	private Dec dec;
	
	public Tournament(Dec dec) {
		this.dec = dec;
	}
	
	public Dec getsomeDecision() {
		return dec;
	}
	
	public void setsize(Integer size) {
		this.size = size;
	}
	
	public Integer getsize() {
		return size;
	}
	
	public void setwinner(Integer winner) {
		this.winner = winner;
	}
	
	public Integer getwinner() {
		return winner;
	}
	
}
